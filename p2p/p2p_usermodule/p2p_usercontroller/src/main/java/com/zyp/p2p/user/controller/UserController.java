@RestController
@RequestMapping("/user")
public class UserController {
    @Autowired
    private UserService userService;

    @RequestMapping("/register")
    public ResultBean register(User user){
        ResultBean resultBean = null;
        try {
            resultBean = userService.register(user);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return resultBean;
    }
}
