public class MD5Util {
    /**
     * 根据指定的盐值和次数生成MD5的值
     * @param source 将要生成MD5的原始内容
     * @param salt 盐值
     * @param time 次数
     * @return
     */
    public static String getMD5(String source,String salt,int time){
        ByteSource saltBytes = ByteSource.Util.bytes(salt);
        return new SimpleHash("MD5",source,saltBytes,time).toString();
    }
}
